#include <stdio.h>

void ft_swap(int *a, int *b)
{
    int temp;
    temp = *a;
    *a = *b;
    *b = temp;

   

}

int main()
{
    int x = 95;
    int y = 5;

    ft_swap(&x, &y);
    printf("%d", x);
    printf("%c", ' ');
    printf("%d", y);
}
